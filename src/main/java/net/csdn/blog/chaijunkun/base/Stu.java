package net.csdn.blog.chaijunkun.base;

public class Stu {
	
	private String name;
	
	private Integer age;
	
	private Boolean sex;

	/**
	 * 获取{name}
	 * @return {name}
	 */
	public String getName() {
		return name;
	}

	/**
	 * 设置{name}
	 * @param name {name}
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * 获取{age}
	 * @return {age}
	 */
	public Integer getAge() {
		return age;
	}

	/**
	 * 设置{age}
	 * @param age {age}
	 */
	public void setAge(Integer age) {
		this.age = age;
	}

	/**
	 * 获取{sex}
	 * @return {sex}
	 */
	public Boolean getSex() {
		return sex;
	}

	/**
	 * 设置{sex}
	 * @param sex {sex}
	 */
	public void setSex(Boolean sex) {
		this.sex = sex;
	}
	
}
